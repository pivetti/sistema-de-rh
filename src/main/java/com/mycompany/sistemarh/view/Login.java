    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        String email = txtEmail.getText();
        String senha = String.valueOf(psfSenha.getPassword());

        UsuarioDAO dao = new UsuarioDAO();

        if (dao.validarLogin(email, senha)) {

            try {
                new Principal().setVisible(true);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao abrir tela principal: " + e);
            }

        } else {
            // Se retornou FALSE
            JOptionPane.showMessageDialog(this, "E-mail ou senha inválidos!");
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

